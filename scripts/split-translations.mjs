// One-shot generator: reads the legacy single-file translations module and
// emits one ESM file per language under src/data/translations/<lang>.js.
//
// Each emitted file exports a `D_<LANG>` object whose keys are the original
// dictionary keys (cat_*, prod_*) and whose values are the original arrays.
// This flips the dictionary shape from D[key][lang] to D[lang][key], so each
// language ends up as a single contiguous chunk that Vite can lazy-load.
//
// Run via: node scripts/split-translations.mjs
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, '..');

const translationsPath = path.join(root, 'src/data/translations.js');
const { D } = await import(pathToFileURL(translationsPath).href);

const langs = new Set();
for (const key of Object.keys(D)) {
    for (const lang of Object.keys(D[key])) langs.add(lang);
}

const outDir = path.join(root, 'src/data/translations');
fs.mkdirSync(outDir, { recursive: true });

const safeName = (lang) => lang.replace(/-/g, '_').toUpperCase();

for (const lang of langs) {
    const slice = {};
    for (const [key, val] of Object.entries(D)) {
        if (Object.prototype.hasOwnProperty.call(val, lang)) slice[key] = val[lang];
    }
    const filePath = path.join(outDir, `${lang}.js`);
    const content =
        `// Auto-generated by scripts/split-translations.mjs — do not hand-edit.\n` +
        `// Source language slice for '${lang}'. See src/data/translations/index.js for the loader.\n` +
        `export const D_${safeName(lang)} = ${JSON.stringify(slice, null, 2)};\n`;
    fs.writeFileSync(filePath, content);
    console.log(`wrote ${path.relative(root, filePath)} (${(fs.statSync(filePath).size / 1024).toFixed(1)} KB)`);
}

console.log(`\nDone. ${langs.size} language slices generated.`);
